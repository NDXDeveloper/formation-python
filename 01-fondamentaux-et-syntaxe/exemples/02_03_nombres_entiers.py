# --- Nombres entiers de base ---
age = 25
annee = 2025
temperature = -5
grand_nombre = 1000000

print(age)           # 25
print(annee)         # 2025
print(temperature)   # -5
print(grand_nombre)  # 1000000

# --- Entiers de taille arbitraire ---
tres_grand_nombre = 123456789012345678901234567890
print(tres_grand_nombre)  # Fonctionne parfaitement !

# --- Underscores pour la lisibilité ---
population = 67_000_000  # 67 millions
print(population)  # Affiche : 67000000
