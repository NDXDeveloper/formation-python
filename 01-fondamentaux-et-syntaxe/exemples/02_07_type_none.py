# --- None représente l'absence de valeur ---
resultat = None
print(resultat)  # Affiche : None

# --- Initialiser puis affecter plus tard ---
nom_utilisateur = None
# ... plus tard dans le programme ...
nom_utilisateur = "Alice"
print(nom_utilisateur)  # Affiche : Alice
